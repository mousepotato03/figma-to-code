  <script>
    // Tab switching functionality
    document.addEventListener('DOMContentLoaded', function() {
      const tabItems = document.querySelectorAll('.tab-item, .tab-link');

      tabItems.forEach(function(tab) {
        tab.addEventListener('click', function() {
          // Remove active class from all tabs
          tabItems.forEach(function(t) {
            t.classList.remove('tab-active');
            const title = t.querySelector('.tab-title');
            if (title) {
              title.classList.remove('tab-title-active');
            }
          });

          // Add active class to clicked tab
          this.classList.add('tab-active');
          const clickedTitle = this.querySelector('.tab-title');
          if (clickedTitle) {
            clickedTitle.classList.add('tab-title-active');
          }
        });
      });

      // Timeline item hover effects
      const timelineItems = document.querySelectorAll('.timeline-item');
      timelineItems.forEach(function(item) {
        item.addEventListener('mouseenter', function() {
          this.classList.add('timeline-item--hover');
        });
        item.addEventListener('mouseleave', function() {
          this.classList.remove('timeline-item--hover');
        });
      });
    });
  </script>
