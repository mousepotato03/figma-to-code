    // Tab interaction for Peptide Discovery section
    document.addEventListener('DOMContentLoaded', function() {
      const tabs = document.querySelectorAll('.peptide-discovery-intro__tab');

      tabs.forEach(function(tab) {
        tab.addEventListener('click', function() {
          // Remove active class from all tabs
          tabs.forEach(function(t) {
            t.classList.remove('peptide-discovery-intro__tab--active');
            const title = t.querySelector('.peptide-discovery-intro__tab-title');
            if (title) {
              title.classList.remove('peptide-discovery-intro__tab-title--active');
            }
          });

          // Add active class to clicked tab
          this.classList.add('peptide-discovery-intro__tab--active');
          const activeTitle = this.querySelector('.peptide-discovery-intro__tab-title');
          if (activeTitle) {
            activeTitle.classList.add('peptide-discovery-intro__tab-title--active');
          }
        });
      });

      // Smooth scroll for anchor links
      document.querySelectorAll('a[href^="#"]').forEach(function(anchor) {
        anchor.addEventListener('click', function(e) {
          e.preventDefault();
          var target = document.querySelector(this.getAttribute('href'));
          if (target) {
            target.scrollIntoView({ behavior: 'smooth' });
          }
        });
      });
    });
