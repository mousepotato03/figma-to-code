  <!-- Scripts (interactions based) -->
  <script>
    // Smooth scroll for anchor links
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
      anchor.addEventListener('click', function (e) {
        e.preventDefault();
        const target = document.querySelector(this.getAttribute('href'));
        if (target) {
          target.scrollIntoView({ behavior: 'smooth' });
        }
      });
    });

    // Timeline year click for smooth scroll
    document.querySelectorAll('.timeline-year').forEach(year => {
      year.addEventListener('click', function() {
        const item = this.closest('.timeline-item');
        if (item) {
          item.scrollIntoView({ behavior: 'smooth', block: 'center' });
        }
      });
    });

    // Highlight current year on scroll
    const timelineItems = document.querySelectorAll('.timeline-item');
    const observerOptions = {
      root: null,
      rootMargin: '-20% 0px -60% 0px',
      threshold: 0
    };

    const observer = new IntersectionObserver((entries) => {
      entries.forEach(entry => {
        if (entry.isIntersecting) {
          timelineItems.forEach(item => item.classList.remove('timeline-item--active'));
          entry.target.classList.add('timeline-item--active');
        }
      });
    }, observerOptions);

    timelineItems.forEach(item => observer.observe(item));
  </script>
</body>
